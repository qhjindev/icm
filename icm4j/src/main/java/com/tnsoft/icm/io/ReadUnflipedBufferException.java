package com.tnsoft.icm.io;

public class ReadUnflipedBufferException extends RuntimeException {

	private static final long serialVersionUID = -6096539193455138487L;

	public ReadUnflipedBufferException() {
		super();
	}

	public ReadUnflipedBufferException(String message) {
		super(message);
	}

	public ReadUnflipedBufferException(Throwable cause) {
		super(cause);
	}

	public ReadUnflipedBufferException(String message, Throwable cause) {
		super(message, cause);
	}
}
